// requireBodyMatchTransfer compares the response body against expected transfer result
func requireBodyMatchTransfer(t *testing.T, body *bytes.Buffer, transferResult db.TransferTxResult) {
	data, err := io.ReadAll(body)
	require.NoError(t, err)

	var gotTransferResult db.TransferTxResult
	err = json.Unmarshal(data, &gotTransferResult)
	require.NoError(t, err)
	require.Equal(t, transferResult, gotTransferResult)
} 